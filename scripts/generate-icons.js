const fs = require('fs');
const path = require('path');

// Create a simple 48x48 PNG icon
function create48x48PNG() {
  // PNG signature + IHDR + IDAT + IEND chunks for a 48x48 blue square
  const png48 = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
    0x00, 0x00, 0x00, 0x0D, // IHDR length
    0x49, 0x48, 0x44, 0x52, // IHDR
    0x00, 0x00, 0x00, 0x30, // width: 48
    0x00, 0x00, 0x00, 0x30, // height: 48
    0x08, 0x02, 0x00, 0x00, 0x00, // bit depth, color type, compression, filter, interlace
    0x00, 0x00, 0x00, 0x00, // IHDR CRC (placeholder)
    0x00, 0x00, 0x00, 0x0C, // IDAT length
    0x49, 0x44, 0x41, 0x54, // IDAT
    0x78, 0x9C, 0x63, 0x60, 0x18, 0x05, 0x00, 0x00, 0x00, 0x01, 0x00, 0x01, // minimal IDAT data
    0x00, 0x00, 0x00, 0x00, // IDAT CRC (placeholder)
    0x00, 0x00, 0x00, 0x00, // IEND length
    0x49, 0x45, 0x4E, 0x44, // IEND
    0xAE, 0x42, 0x60, 0x82  // IEND CRC
  ]);
  
  return png48;
}

